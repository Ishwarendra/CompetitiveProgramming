using i64 = long long;

void solve()
{
    int n, m;
    std::cin >> n >> m;

    std::vector<int> a(m);
    int ans = 0;
    for (int i = 0; i < m; i++)  
    {
        std::cin >> a[i];
        for (int j = i - 1; j >= 0; j--)
            ans += a[j] < a[i];
    }

    std::cout << ans << "\n";
}

int main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t;
    std::cin >> t;
    
    while (t--)
        solve();
    
    return 0;
}