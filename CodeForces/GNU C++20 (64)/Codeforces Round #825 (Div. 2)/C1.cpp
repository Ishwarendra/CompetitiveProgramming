using i64 = long long;

void solve()
{
    int n;
    std::cin >> n;

    std::vector<int> a(n);
    for (int i = 0; i < n; i++)  
        std::cin >> a[i];

    int l = 0, r = 0;
    i64 ans = 0;
    while (l < n and r < n)
    {
        if (a[r] >= r - l + 1)
        {
            ans += r - l + 1;
            r++;
        }
        else
            l++;
    }

    std::cout << ans << "\n";
}

int main()
{
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);

    int t = 1;
    std::cin >> t;
    
    while (t--)
        solve();
    
    return 0;
}